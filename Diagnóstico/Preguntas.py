#3-. Explique y ejemplifique el concepto de objeto.
# Un objeto en programación es una instancia específica de una clase. Las clases actúan como plantillas o moldes para crear objetos con atributos y comportamientos específicos. Cada objeto tiene sus propios valores para los atributos definidos en su clase.
# Creamos dos objetos de la clase Perro: En este caso, perro1 y perro2 son objetos de la clase Perro. Cada uno tiene su propio nombre y edad, definidos por los valores que pasamos al crear los objetos. Por ejemplo, perro1 tiene el nombre "Napo" y una edad de 5 años, mientras que perro2 tiene el nombre "Roy" y una edad de 3 años. Cada objeto de la clase Perro puede ladrar, pero el mensaje impreso varía según el nombre del perro. Esto demuestra cómo los objetos son instancias específicas de una clase y pueden tener atributos y comportamientos únicos.